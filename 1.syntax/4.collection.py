# *** Словарь (dict) ***

# пара "ключ-значение"
# {ключ_1:значение_1, ключ_2:значение_2}

# создание словаря
my_dict = {10:3.14, "abc":[1,2,3]}

# чтение значений 
val = my_dict["abc"]
val = my_dict[10]

